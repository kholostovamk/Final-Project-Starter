package model.enums;

/**
 * Enum representing age groups for pets.
 * Each type of pet uses different numbers for the same group.
 */
public enum AgeGroup {
    PUPPY,     // Typically 0–1 year (for dogs)
    YOUNG,     // Typically 1–3 years
    ADULT,     // Typically 4–7 years
    SENIOR     // 8+ years
}

